public class Ifelse {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        if (a > b) {//в случае если а больше б мы выводим
            System.out.println("A is a greater than B");//первый вариант
        } else {//или мы выводим
            System.out.println("B is greater");//второй вариант
        }
    }
}
